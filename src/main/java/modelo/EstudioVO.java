package modelo;

public class EstudioVO {
	private int alestudio;
	private int aldni;
	private String altipo;
	private String alres;
	private int almedico;
	
	public int getAlestudio() {
		return alestudio;
	}
	public void setAlestudio(int alestudio) {
		this.alestudio = alestudio;
	}
	public int getAldni() {
		return aldni;
	}
	public void setAldni(int aldni) {
		this.aldni = aldni;
	}
	public String getAltipo() {
		return altipo;
	}
	public void setAltipo(String altipo) {
		this.altipo = altipo;
	}
	public String getAlres() {
		return alres;
	}
	public void setAlres(String alres) {
		this.alres = alres;
	}
	public String getAlmedic() {
		return almedic;
	}
	public void setAlmedic(String almedic) {
		this.almedic = almedic;
	}
	public int getAlmedico() {
		return almedico;
	}
	public void setAlmedico(int almedico) {
		this.almedico = almedico;
	}
	private String almedic;


}
